int	ft_str_is_alpha(char *str)
{
	char	test;
	int		i;

	i = 0;
	while (str[i])
	{
		test = str[i];
		if ((test < 'a' && test > 'Z') || (test < 'A') || (test > 'z'))
		{
			return (0);
		}
		i++;
	}
	return (1);
}
